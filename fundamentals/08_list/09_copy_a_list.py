# Copy list
# We can copy list with copy() method
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist)

# Use list() method
thislist = ["apple", "banana", "cherry"]
mylist = list(thislist)
print(mylist)

# Use the slice opertator
thislist = ["apple", "banana", "cherry"]
mylist = thislist[:]
print(mylist)