thistuple = ("apple", "banana", "cherry")
for x in thistuple:
    print(x)

# Loop through the index number using for
print("this loop through number using for:")
for i in range(len(thistuple)):
    print(thistuple[i])

# Using while loop

print("this loop through number using while:")
i = 0
while (i < len(thistuple)):
    print(thistuple[i])
    i = i + 1
